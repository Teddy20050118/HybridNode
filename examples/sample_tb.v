`timescale 1ns / 1ps

//================================================================================
// Testbench for ATE Module
// 此測試台用於驗證 ATE 模組的功能
// 測試資料來源：tb1.map（4096 筆 8-bit 十六進位數據）
//================================================================================

module sample_tb;

    //============================================================================
    // 訊號宣告
    //============================================================================
    
    // 時脈與重置訊號
    reg clk;
    reg reset;
    
    // ATE 模組的輸入訊號
    reg [7:0] pix_data;      // 8-bit 像素資料輸入
    reg type;                // 類型選擇訊號
    
    // ATE 模組的輸出訊號
    wire bin;                // 二值化輸出
    wire [7:0] threshold;    // 8-bit 閾值輸出
    
    // 測試資料儲存空間
    reg [7:0] test_patterns [0:4095];  // 儲存 4096 筆測試資料
    
    // 迴圈計數器
    integer i;

    //============================================================================
    // 例現化待測模組 (Design Under Test)
    //============================================================================
    
    ATE uut (
        .clk(clk),
        .reset(reset),
        .pix_data(pix_data),
        .type(type),
        .bin(bin),
        .threshold(threshold)
    );

    //============================================================================
    // 時脈產生器
    // 週期：10ns (頻率：100MHz)
    //============================================================================
    
    initial begin
        clk = 1'b0;
    end
    
    always #5 clk = ~clk;  // 每 5ns 反轉一次，形成 10ns 週期

    //============================================================================
    // 測試主程式
    //============================================================================
    
    initial begin
        // VCD 波形檔案輸出設定（Stage 3 模擬分析所需）
        $dumpfile("dump.vcd");
        $dumpvars(0, sample_tb);
        
        // 顯示測試開始訊息
        $display("========================================");
        $display("ATE Module Testbench");
        $display("開始時間：%0t ns", $time);
        $display("========================================");
        
        // 讀取測試資料（十六進位格式）
        $readmemh("tb1.map", test_patterns);
        $display("已載入測試資料：tb1.map (4096 筆)");
        
        // 初始化所有輸入訊號
        reset = 1'b1;        // 啟動重置
        pix_data = 8'h00;    // 像素資料初始化為 0
        type = 1'b0;         // 類型訊號初始化為 0
        
        // 維持重置訊號 20ns
        #20;
        reset = 1'b0;        // 釋放重置
        $display("重置完成，開始餵入測試資料 (%0t ns)", $time);
        
        // 等待一個時脈週期讓系統穩定
        @(posedge clk);
        
        // 依序將 4096 個像素資料餵給 ATE 模組
        for (i = 0; i < 4096; i = i + 1) begin
            @(posedge clk);  // 在時脈上升緣更新資料
            pix_data = test_patterns[i];
            
            // 每 512 筆資料顯示一次進度
            if (i % 512 == 0) begin
                $display("進度：%0d / 4096 筆資料已送出 (%0t ns)", i, $time);
            end
        end
        
        // 所有資料送出完畢
        $display("========================================");
        $display("測試資料送出完成：4096 筆");
        $display("完成時間：%0t ns", $time);
        $display("========================================");
        
        // 額外等待 100ns 讓所有訊號穩定
        #100;
        
        // 結束模擬
        $display("模擬結束");
        $finish;
    end
    
    //============================================================================
    // 監控與除錯（選用）
    //============================================================================
    
    // 監控關鍵訊號變化
    initial begin
        $monitor("時間=%0t | pix_data=%h | bin=%b | threshold=%h", 
                 $time, pix_data, bin, threshold);
    end

endmodule
